console.log("using a for loop");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}
console.log("using a while loop");
let i = 2;
while (i <= 10) {
    console.log(i);
    i++;
}

console.log("using a do while loop loop");
let j = 1;
do {
    console.log(i);
    j++;
} while (j <= 5);